import json
from arcadepy import Arcade

client = Arcade()  # Automatically finds the `ARCADE_API_KEY` env variable

USER_ID = "{arcade_user_id}"
TOOL_NAME = "DatadogApi.CreateIncidentNotificationTemplate"

auth_response = client.tools.authorize(
    tool_name=TOOL_NAME,
    user_id=USER_ID,
)

if auth_response.status != "completed":
    print(f"Click this link to authorize: {auth_response.url}")

# Wait for the authorization to complete
client.auth.wait_for_completion(auth_response)

tool_input = {
    'notification_content_body': 'This is a notification regarding the incident that occurred.',
    'notification_subject': 'Incident Alert: Immediate Attention Required',
    'notification_template_category': 'Critical',
    'notification_template_name': 'Critical Incident Notification',
    'resource_type_notification_template': 'notification_templates'
}

response = client.tools.execute(
    tool_name=TOOL_NAME,
    input=tool_input,
    user_id=USER_ID,
)
print(json.dumps(response.output.value, indent=2))
